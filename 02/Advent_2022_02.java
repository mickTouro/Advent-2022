    public static int calcScore(List<List<Character>> list){
        int score = 0;
        for(List<Character> line : list){
            score += calcScore(line.get(0), line.get(1));
        }
        return score;
    }

    public static int calcScore(Character a, Character b){
        int elf = getNum(a);
        int me = getNum(b);

        if( elf == me ){
            return elf + 3;
        }
        else if (
            (elf == 1 && me == 2) ||
            (elf == 2 && me == 3) ||
            (elf == 3 && me == 1)){
            return me + 6;
        }
        else 
            return me;
        
    }



