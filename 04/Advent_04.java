    public static int getFullyContainCount(String pairs){
        int count = 0;
        for(String s : pairs.split("\n")){
            String[] ranges = s.split(",");

            String[] range1 = ranges[0].split("-");
            String[] range2 = ranges[1].split("-");

            if(Integer.parseInt(range1[0]) <= Integer.parseInt(range2[0]) && Integer.parseInt(range1[1]) >= Integer.parseInt(range2[1])) count++;
            else if(Integer.parseInt(range2[0]) <= Integer.parseInt(range1[0]) && Integer.parseInt(range2[1]) >= Integer.parseInt(range1[1])) count++;
        }
        return count;
    }

