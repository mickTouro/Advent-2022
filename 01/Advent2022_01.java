    // returns the food count of the elf carrying the most
    public static int getMaxFood(List<String> foodList){

        int maxFood = 0;
        int currFood = 0;
        for( String x : foodList){
            if(x=="") { currFood = 0;}
            else{
                currFood += Integer.parseInt(x);
                if(currFood > maxFood) { maxFood = currFood;}
            }
        }
        return maxFood;
    }
